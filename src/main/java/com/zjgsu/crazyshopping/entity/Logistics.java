package com.zjgsu.crazyshopping.entity;

public class Logistics {
    private String id;
    private Integer ordersId;

    public Logistics() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public Integer getOrdersId() {
        return ordersId;
    }

    public void setOrdersId(Integer ordersId) {
        this.ordersId = ordersId;
    }
}
